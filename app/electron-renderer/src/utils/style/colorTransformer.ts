export abstract class ColorTransformer {
    static translucent(color: string, level: string) {
        return color + level;
    }

    static translucent00(color: string) {
        return `${color}00`;
    }

    static translucent10(color: string) {
        return `${color}10`;
    }

    static translucent20(color: string) {
        return `${color}20`;
    }

    static translucent30(color: string) {
        return `${color}30`;
    }

    static translucent40(color: string) {
        return `${color}40`;
    }

    static translucent50(color: string) {
        return `${color}50`;
    }

    static translucent60(color: string) {
        return `${color}60`;
    }

    static translucent70(color: string) {
        return `${color}70`;
    }

    static translucent80(color: string) {
        return `${color}80`;
    }

    static translucent90(color: string) {
        return `${color}90`;
    }

    static translucentA0(color: string) {
        return `${color}a0`;
    }

    static translucentB0(color: string) {
        return `${color}b0`;
    }

    static translucentC0(color: string) {
        return `${color}c0`;
    }

    static translucentD0(color: string) {
        return `${color}d0`;
    }

    static translucentE0(color: string) {
        return `${color}e0`;
    }

    static translucentF0(color: string) {
        return `${color}f0`;
    }
}
